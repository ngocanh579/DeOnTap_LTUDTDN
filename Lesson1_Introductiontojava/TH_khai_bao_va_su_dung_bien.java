public class TH_khai_bao_va_su_dung_bien {
    public static void main(String[] args) {
        // Biến i kiểu số nguyên, có giá trị là 10
        // Biến f kiểu số thực, có giá trị là 20.5
        // Biến d kiểu số thực lớn, có giá trị là 20.5
        // Biến b kiểu logic, có giá trị là true
        // Biến c kiểu ký tự, có giá trị là 'a'
        // Biến s kiểu chuỗi, có giá trị là "Hà Nội".
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        String c = "a";
        String s = "Hà Nội";
        System.out.println("kiểu số nguyên : " + i);
        System.out.println("kiểu số thực : " + f);
        System.out.println("kiểu số thực lớn : " + d);
        System.out.println("kiểu logic : " + b);
        System.out.println("kiểu ký tự : " + c);
        System.out.println("kiểu chuỗi : " + s);

    }

}
